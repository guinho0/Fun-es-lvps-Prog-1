def main():
  for n in range(1,1000000):
    cm = n//10**5
    dm = n%10**5//10**4
    um = n%10**5%10**4//10**3
    c = n%10**5%10**4%10**3//100
    d = n%10**5%10**4%10**3%100//10
    u = n%10**5%10**4%10**3%100%10%100%10%10
    l = len(str(n))
    if (cm**l + dm**l +  um**l + c**l + d**l + u**l) == n:
        print(n)
        
    
  return 0
main()